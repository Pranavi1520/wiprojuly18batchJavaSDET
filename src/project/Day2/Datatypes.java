package project.Day2;

public class Datatypes {

	public static void main(String[] args) {
		//boolean
		boolean b = true;
		System.out.println(b);
		//char
		char c = 'a';
		System.out.println(c);
		//byte
		byte b1 = 67;
		System.out.println(b1);
		//string
		String s1 = "new delhi";
		System.out.println(s1);
		//short
		Short s2=-31677;
		System.out.println(s2);
	}

}
